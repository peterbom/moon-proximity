import { JPLDE } from "./jplde";

export function testAll(jplde: JPLDE) {
  ephemerisTestItems.map(asEphemerisTest).forEach((test) => testEphemeris(jplde, test));
}

function testEphemeris(jplde: JPLDE, test: EphemerisTest) {
  const t1 = get(jplde, test.target, test.jd, test.x);
  const t2 = get(jplde, test.center, test.jd, test.x);

  const v = t1 - t2;

  const error =
    test.target === 14 || test.target === 15 ? Math.abs(v - test.expected) : Math.abs(v / au - test.expected);

  if (error > 1.0e-11 || isNaN(error)) {
    throw new Error(`Fail: ${test.target}\t${test.center}\t${test.x}\t${test.expected}\t${v / au}\tDiff=${error}`);
  }
}

const planetsBySeriesIndex = [0, 0, 1, 13, 3, 4, 5, 6, 7, 8, 9, 10, 12, 2, 11, 12, 0, 14];
const au = 0.149597870699999988e9;

function get(jplde: JPLDE, series: number, jd: number, x: number) {
  if (series === 12 || series === 0) {
    return 0;
  }

  if (series === 3) {
    const emb = jplde.getPlanet(2, jd);
    const moon = jplde.getPlanet(9, jd);
    const earth = jplde.getEarthPositionFromEMB(emb, moon);
    return earth[x - 1];
  }

  if (series === 10) {
    const emb = jplde.getPlanet(2, jd);
    const moon = jplde.getPlanet(9, jd);
    const earth = jplde.getEarthPositionFromEMB(emb, moon);
    for (let i = 0; i < 6; i++) {
      moon[i] += earth[i];
    }
    return moon[x - 1];
  }

  const results = jplde.getPlanet(planetsBySeriesIndex[series], jd);
  return results[x - 1];
}

